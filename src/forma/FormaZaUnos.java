    private void popuniComboBox() {
        for(TipMasine tm: TipMasine.values()){
            jComboBoxTipMasine.addItem(tm);
        }
        
       List<Lokacija> sveLokacije = Controller.getInstance().vratiSveLokacijeIzBaze();
       
       for(Lokacija l: sveLokacije){
           jComboBoxLokacija.addItem(l);
       }
       
       List<Alat> sviAlati = Controller.getInstance().vratiSveAlate();
       for(Alat a: sviAlati){
           jComboBoxAlat.addItem(a);
       }
       
       
    }

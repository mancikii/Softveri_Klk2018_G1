    private void jButtonSacuvajMasinuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajMasinuActionPerformed

        try {
        Inzenjer ulogovani = Controller.getInstance().getUlogovani();
        String imePrezime = ulogovani.getIme()+" "+ulogovani.getPrezime();
        String naziv = jTextFieldNaziv.getText();
        String proizvodjac = jTextFieldProizvodjac.getText();
        int radniVek = Integer.parseInt(jTextFieldRadniVek.getText());
        int godPr = Integer.parseInt(jTextFieldProizvodjac.getText());
        if(godPr <1900 || godPr>2018){
            JOptionPane.showConfirmDialog(null, "GRESKA godina proizvodnje");
            return;
        }
        TipMasine tip = (TipMasine) jComboBoxTipMasine.getSelectedItem();
        Lokacija lokacija = (Lokacija) jComboBoxLokacija.getSelectedItem();
        int idLokacija = lokacija.getId();
        boolean postojiMasinaNaLokaciji = Controller.getInstance().postojiMasina(idLokacija);
        if(postojiMasinaNaLokaciji){
            JOptionPane.showMessageDialog(null, "GRESKA MASINA");
            return;
        }
        SimpleDateFormat format = new SimpleDateFormat("dd.MM.yyyy");
        Date datum = format.parse(jTextFieldDatum.getText());
        
        if(datum.before(new Date())){
          JOptionPane.showMessageDialog(this, "GRESKA DATUM");
          return;
        }
        
        Masina m = new Masina(-1, naziv, datum, godPr, radniVek, proizvodjac, tip, lokacija, imePrezime);
        ModelTabeleAlat mta = (ModelTabeleAlat) jTableAlat.getModel();
        List<Alat> alati = mta.getAlati();
        m.setAlati(alati);
        boolean uspesno = Controller.getInstance().ubaciMasinuUBazu(m);
         
        
        } catch (ParseException ex) {
            Logger.getLogger(FormaZaUnos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButtonSacuvajMasinuActionPerformed



